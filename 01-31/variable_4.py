# 변수 타입 알아보기
    # 파이썬 변수 이름은 snakecase
a_string = "like this" #문자열
a_number = 3 #정수형
a_float = 3.12 #실수형(Float)
a_boolean = True
a_none = None

 # ()가 어떤 타입인지 나타내라 = print(type())
print(type(a_string))
    #>>><class 'str'>
print(type(a_number))
    #>>><class 'int'>
print(type(a_float))
    #>>><class 'float'>
print(type(a_boolean))
    #>>><class 'bool'>
print(type(a_none))
    #>>><class 'NoneType'> = empty = 존재하지 않는다(참,거짓과 다름)