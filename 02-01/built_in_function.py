#많이 쓰이는 빌트인 함수 | int() bool() str() float()
#int() / type을 바꿔줌
age = "18"
print(type(age))
    #>>><class 'str'>
n_age = int(age) #n_age라는 것은 age를 int 함수에 넣은 것이다 = age의 타입을 integer로 바꾼것이다.
print(type(n_age))
    #>>><class 'int'>
